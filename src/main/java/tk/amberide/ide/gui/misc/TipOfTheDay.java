    public static boolean showTipOfTheDay() {
        TipOfTheDay tips = new TipOfTheDay(Amber.getUI());
        tips.setVisible(true);
        return tips.startupCheckBox.isSelected();
    }

    protected static String randomTip() {
        try {
            return String.format(BASE_DOCUMENT, TIPS != null ? TIPS.get(new Random().nextInt(TIPS.size())).getValue() : TIPS_MISSING);
        } catch (Exception e) {
            return TIPS_MISSING;
        }
    }

