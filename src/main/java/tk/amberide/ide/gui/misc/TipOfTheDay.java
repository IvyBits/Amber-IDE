    protected static final String BASE_DOCUMENT = "<html><body><font size=\"5\">%s</font></html></body>";
    protected static Elements TIPS;

    static {
        InputStream is = ClassLoader.getSystemResourceAsStream("tips/Tips.xml");
        if (is != null) {
            try {
                Document tips = new Builder().build(is);
                TIPS = tips.getRootElement().getChildElements("tip");
            } catch (Exception e) {
                e.printStackTrace();
                TIPS = null;
            }
        }

    }

